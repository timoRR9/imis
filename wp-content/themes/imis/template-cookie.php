<h5>Qu'est-ce qu'un cookie ?</h5><br />

Les cookies ont différentes fonctions. Un cookie peut permettre à celui qui l'a déposé de reconnaître un internaute, d'une visite à une autre, grâce à un identifiant unique. Certains cookies peuvent aussi être utilisés pour stocker le contenu d'un panier d'achat, d'autres pour enregistrer les paramètres de langue d'un site, d'autres encore pour faire de la publicité ciblée.<br />
<br /><h5>Quel est le cadre juridique applicable ?</h5><br />

En modifiant l'article 5(3) de la directive 2002/58/CE par l'adoption de la directive 2009/136/CE, le législateur européen a posé le principe selon lequel le stockage d'informations sur l'équipement d'un utilisateur ou l'accès à des informations déjà stockées, ne devaient être mis en œuvre qu'avec le consentement préalable de l'utilisateur, sauf si ces actions sont strictement nécessaires au fournisseur pour la délivrance d'un service expressément demandé par l'abonné ou l'utilisateur. L'article 32-II de la loi du 6 janvier 1978 reprend ce principe.<br />
